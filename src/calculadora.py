num1 = int(input("Digite um inteiro: "))
num2 = int(input("Digite outro inteiro: "))
operacao = input("Digite a operação (+,-,*,/):")

if operacao == "+":
    print(num1+num2)

if operacao == "-":
    print(num1-num2)

if operacao == "*":
    print(num1*num2)

if operacao == "/":
    print(num1/num2)
